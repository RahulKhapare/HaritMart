package grocery.app.model;

public class SliderModel {

    String title;
    String url;
    String new_window;
    String image;
    String image_alt_text;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getNew_window() {
        return new_window;
    }

    public void setNew_window(String new_window) {
        this.new_window = new_window;
    }

    public String getImage_alt_text() {
        return image_alt_text;
    }

    public void setImage_alt_text(String image_alt_text) {
        this.image_alt_text = image_alt_text;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
